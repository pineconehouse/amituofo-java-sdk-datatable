package com.amituofo.datatable;

public class DefinitionException extends Exception {
	private static final long serialVersionUID = 627144628869771015L;

	public DefinitionException() {
		super();
	}

	public DefinitionException(String msg) {
		super(msg);
	}

	public DefinitionException(Throwable cause) {
		super(cause);
	}

	public DefinitionException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
